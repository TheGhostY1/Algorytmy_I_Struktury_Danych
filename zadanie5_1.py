def fibonacci(n):
   if n <= 1:
       return n
   else:
       return(fibonacci(n - 1) + fibonacci(n - 2))

element = int(input("Ile elementów?: "))

if element <= 0:
   print("Please enter a positive integer!")
else:
   print("Ciąg Fibonacciego:")
   for i in range(element):
       print(fibonacci(i))
